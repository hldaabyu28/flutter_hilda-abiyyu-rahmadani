// function main
void main (){
  // deklarasi variabel dan inisiasi nilai 
  int nilai = 80 ;
  // switch case dengan mencari inputan nilai dan hasil yang akan di print  
 
  switch (nilai){
    case > 70 :
    print("Nilai A");
    break;
    case > 40 :
    print("Nilai B");
    break;
    case > 0 :
    print("Nilai C");
    break;
    default:
    print("");

  }
}